let atual, minima, media, maxima

atual = parseFloat(prompt("Quantidade atual de produtos no estoque: "))
maxima = parseFloat(prompt("Quantidade máxima de produtos no estoque: "))
minima = parseFloat(prompt("Quantidade mínima de produtos no estoque: "))

media <= (maxima - minima) / 2

if (media >= atual){
    alert("Não efetuar compra")
}
else{
    alert("Efetuar compra")
}
