let salario, vendas, comissao, total;

salario = parseFloat(prompt("Qual o seu salário fixo: "));
vendas = parseFloat(prompt("Valor das vendas: "));

if (vendas = 1500){
comissao = vendas * 0.05
total = salario + comissao
}
else{
    comissao = vendas * 0.03
    total = salario + comissao
}

alert("Salário total: " + "R$" + total);
